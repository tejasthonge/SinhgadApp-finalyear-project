class StudentModel {
  final String name;
  final String prn;
  final int semester;
  final double cgpa;
  final int attendanceCount;
  final String branch;

  StudentModel({
    required this.name,
    required this.prn,
    required this.semester,
    required this.cgpa,
    required this.attendanceCount,
    required this.branch,
  });
}